#include <stdio.h>
#include <stdlib.h>
#include <niftyled.h>




int main(int argc, char *argv[])
{
        nft_log_level_set(L_DEBUG);
        
        /* try to initialize dummy hardware */
        LedHardware *a;
        if(!(a = led_hardware_new("nlo01", "dummy")))
        {
                NFT_LOG(L_ERROR, "Hardware creation FAILED");
                return -1;
        }


        if(!led_hardware_init(a, "*", 16, "RGB u8"))
        {
                NFT_LOG(L_ERROR, "failed to initialize hardware");
                return -1;
        }

        /* set gain */
        if(!(led_hardware_set_gain(a, 0, 0x6FFF)))
        {
                NFT_LOG(L_ERROR, "Failed to send gain");
                return -1;
        }
        
        
        led_hardware_deinit (a);
        return 0;

}
